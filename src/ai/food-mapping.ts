export function getMatchedFoods(label: string): FoodItem[] {
  const normalized = label.toLowerCase().trim();
  
  // Try direct match first (case-insensitive lookup in the map)
  // The map keys are the original CSV names (case sensitive usually, but let's be safe)
  
  // We iterate keys because we stored exact keys
  for (const [key, val] of Object.entries(FOOD_LABEL_MAPPING)) {
    if (key.toLowerCase() === normalized) {
       return val
        .map(name => FOOD_DATABASE.find(food => food.name === name))
        .filter((food): food is FoodItem => Boolean(food));
    }
  }

  // Fallback: search in database directly by name (since database names contain the English name now)
  const found = FOOD_DATABASE.find(f => f.name.toLowerCase().includes(normalized));
  if (found) return [found];

  return [];
}
